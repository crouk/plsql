REM ************ CREACION DE TABLAS

CREATE TABLE XF_ARTISTAS ( 
  ID_ARTISTA        NUMBER (4) NOT NULL PRIMARY KEY, 
  NOMBRE            VARCHAR2 (20), 
  NUM_INTEGRANTES   NUMBER (2), 
  ESTILO            VARCHAR2 (10) CHECK (ESTILO IN ('ROCK','POP','CLASICA','LATINA','OTRO')), 
  TARIFA_ACTUACION  NUMBER (8)   ) ; 

CREATE TABLE XF_GRABACIONES ( 
  ID_GRABACION      NUMBER (4) NOT NULL PRIMARY KEY, 
  TITULO            VARCHAR2 (20), 
  TIPO              VARCHAR2 (10) CHECK (TIPO IN ('CD','LP','DVD','CD-DVD','VHS')), 
  FECHA_EDICION     DATE) ; 

CREATE TABLE XF_ARTISTAS_GRABACIONES ( 
  ID_ARTISTA        NUMBER (4) NOT NULL REFERENCES XF_ARTISTAS, 
  ID_GRABACION      NUMBER (4) NOT NULL REFERENCES XF_GRABACIONES, 
  NUM_CANCIONES     NUMBER (3),
  CONSTRAINT PK_XF_AG PRIMARY  KEY(ID_ARTISTA,ID_GRABACION)) ; 

CREATE TABLE XF_CANCIONES ( 
  ID_CANCION        NUMBER (4) NOT NULL PRIMARY KEY, 
  TITULO            VARCHAR2(20),
  ESTILO            VARCHAR2(10) CHECK (ESTILO IN ('ROCK','POP','CLASICA','LATINA','OTRO')), 
  DURACION          NUMBER(5,2), 
  CANON_SGAE        NUMBER(5,2),
  ID_GRABACION      NUMBER(4) REFERENCES XF_GRABACIONES ); 

CREATE TABLE XF_ACTUACIONES (
  ID_ACTUACION      NUMBER(4) NOT NULL PRIMARY KEY,
  FECHA             DATE,
  POBLACION         VARCHAR2(15),
  RECINTO           VARCHAR2(15),
  PRECIO_ENTRADA    NUMBER(5,2),
  AFORO_MAXIMO      NUMBER(5),
  ENTRADAS_VENDIDAS NUMBER(5),
  ID_ARTISTA        NUMBER(4) REFERENCES XF_ARTISTAS);

CREATE TABLE XF_ACTUACIONES_CANCIONES (
  ID_ACTUACION      NUMBER(4) NOT NULL REFERENCES XF_ACTUACIONES,
  ID_CANCION        NUMBER (4) NOT NULL REFERENCES XF_CANCIONES, 
  ORDEN             NUMBER(2),
  CONSTRAINT PK_XF_AC PRIMARY  KEY(ID_ACTUACION,ID_CANCION));





